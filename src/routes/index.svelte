<h1>Hermetiza</h1>
<dl>
  <dt>The principle of mentalism</dt>
  <dd>The All is Mind; the universe is Mental.</dd>
  <dt>The principle of correspondence</dt>
  <dd>As above, so below; as below, so above.</dd>
  <dt>The principle of vibration</dt>
  <dd>Nothing rests; everything moves; everything vibrates.</dd>
  <dt>The principle of polarity</dt>
  <dd>
    Everything is dual; everything has poles; everything has its pair of
    opposites; like and unlike are the same; opposites are identical in nature,
    but different in degree; extremes meet; all truth are but half-truths; all
    paradoxes may be reconciled.
  </dd>
  <dt>The principle of rhythm</dt>
  <dd>
    Everything flows, out and in; everything has its tides; all things rise and
    fall; the pendulum-swing manifests in everything; the measure of the swing
    to the right is the measure of the swing to the left; rhythm compensates.
  </dd>
  <dt>The principle of cause and effect</dt>
  <dd>
    Every cause has its effect; every effect has its cause; everything happens
    according to law; chance is but a name for law not recognized; there are
    many planes of causation, but nothing escapes the law.
  </dd>
  <dt>The principle of gender</dt>
  <dd>
    Gender is in everything; everything has its masculine and feminine
    principles; gender manifests on all planes.
  </dd>
</dl>

